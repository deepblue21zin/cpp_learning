//a의 좌표 구하기 : 가로 위치 = (a-1)/4, 세로 위치 = (a-1) % 4
//b의 좌표 구하기 : 가로 위치 = (b-1) / 4, 세로위치 = (b-1) % 4
//최종 거리 구하기  : 가로 거리 = abs(x1-x2), 세로 거리 = abs(y1-y2)
